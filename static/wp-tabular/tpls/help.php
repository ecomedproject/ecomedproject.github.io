<h2>How to use WP Table</h2>

<Br/>

Simply use the following short-code inside page or post content to display WP Table:
<br/>
<br/>
<code style="font-size: 14pt;">[ahm-wp-tabular id=table_id template=template_name]</code>
<br/>
<br/>
replace:<br/>
<strong>table_id</strong> with <a href='edit.php?post_type=wp-tabular'>the id of table you created</a><br/>

replace template_name with any of following available template name:<br/>

<ul style="list-style: square;margin-left:15px;font-size:14pt">
<li>style1-blue</li>
<li>style1-green</li>
<li>style1-orange</li>
<li>style2-gradient</li>
<li>style2-gray</li>
<li>style2-green</li>
<li>style2-green</li>
<li>style3</li>
<li>style4-blue</li>
<li>style4-gray</li>
</ul>


